function checkDay(dom1, obj) {
	$(dom1).each(function (item, index) {
		var $this = $(this);
		var toDay = dayjs(new Date());
		//選擇 section
		var sectionBlock = 'section' + item + '';
		for (var i = 0; i < obj[sectionBlock].length; i++) {
			var thisDeadLine = dayjs(obj[sectionBlock][i].deadline);
			if (toDay.isBefore(thisDeadLine)) {
				$this.attr('href', obj[sectionBlock][i].link).html('<img src="' + obj[sectionBlock][i].img + '" class="radius_M">')
				break;
			}
		}
	})

}
